async def main():
    svc = GuidelineService()
    print(f"Seeding {len(DEMO_GUIDELINES)} demo guidelines...")

    for title, text in DEMO_GUIDELINES.items():
        guideline = await svc.ingest_text(title=title, text=text, source=GuidelineSource.LOCAL)
        print(f"  ✅ {guideline.title_ru}")
        print(f"     Sections: {len(guideline.sections)}, Recommendations: {len(guideline.recommendations)}")

    print(f"\nDone! {len(svc.list_guidelines())} guidelines indexed.")
    print("Start the API server and try:")
    print("  curl -X POST http://localhost:8000/api/v1/clinical/guidelines/search")
    print('    -H "Content-Type: application/json"')
    print('    -d \'{"query": "пневмония"}\'')


if __name__ == "__main__":
    asyncio.run(main())
